// ─── Creature definitions ───────────────────────────────────────────────────

struct Creature {
    name: &'static str,
    frames: &'static [&'static [&'static str]],
    width: usize,
    base_color: fn(char) -> (f64, f64, f64),
    wave_k: f64,
    wave_speed: f64,
    amp: f64,
    frame_fps: f64,
}

